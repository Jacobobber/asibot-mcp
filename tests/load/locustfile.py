class McpUser(HttpUser):
    """Simulates a single user interacting with the Asibot MCP server.

    Each user:
    1. Picks a pre-seeded API key from the CSV pool
    2. Initializes an MCP session (authenticate + get session ID)
    3. Lists available tools
    4. Makes tool calls with realistic parameters
    5. Occasionally triggers rate limiting by bursting requests
    6. Reuses the session ID for subsequent requests (session caching)
    """

    # Think time between requests: 1-5 seconds simulates real user pauses
    wait_time = between(1.0, 5.0)

    # Track per-user state
    api_key: str = ""
    user_id: str = ""
    session_id: str | None = None
    _initialized: bool = False
    _request_counter: int = 0

    def on_start(self):
        """Called when a simulated user starts. Pick a pre-seeded API key and initialize."""
        keys = _load_seeded_keys()
        entry = random.choice(keys)
        self.api_key = entry["api_key"]
        self.user_id = entry["user_id"]
        self.session_id = None
        self._initialized = False
        self._request_counter = 0
        self._initialize_session()

    def _auth_headers(self) -> dict[str, str]:
        """Return headers with Bearer auth and optional session ID."""
        headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json",
            "Accept": "application/json, text/event-stream",
        }
        if self.session_id:
            headers["Mcp-Session-Id"] = self.session_id
        return headers

    def _send_mcp_request(self, payload: dict, name: str) -> Any:
        """Send a JSON-RPC request to the MCP endpoint.

        Handles session ID extraction from response headers and tracks
        success/failure for Locust metrics.
        """
        self._request_counter += 1
        headers = self._auth_headers()

        with self.client.post(
            MCP_PATH,
            json=payload,
            headers=headers,
            name=name,
            catch_response=True,
        ) as response:
            # Extract session ID from response headers if present
            new_session_id = response.headers.get("Mcp-Session-Id")
            if new_session_id:
                self.session_id = new_session_id

            if response.status_code == 429:
                # Rate limited -- expected under load, mark as success for
                # tracking but record in custom metrics
                _custom_metrics["rate_limit_hits"].append(response.elapsed.total_seconds() * 1000)
                response.success()
                return {"rate_limited": True}

            if response.status_code >= 400:
                response.failure(f"HTTP {response.status_code}: {response.text[:200]}")
                return None

            # Parse the response -- MCP over streamable-http may return
            # SSE events or plain JSON depending on the server mode
            try:
                content_type = response.headers.get("Content-Type", "")
                if "text/event-stream" in content_type:
                    # Parse SSE: extract the last JSON data line
                    body = response.text
                    result = _parse_sse_response(body)
                    if result is not None:
                        response.success()
                        return result
                    # If no parseable result, still mark success (could be
                    # a notification-only stream)
                    response.success()
                    return {"sse": True, "raw": body[:500]}
                else:
                    result = response.json()
                    if "error" in result:
                        # JSON-RPC error -- still a valid protocol response
                        error_msg = result["error"].get("message", "unknown")
                        response.success()
                        return result
                    response.success()
                    return result
            except Exception as e:
                response.failure(f"Parse error: {e}")
                return None

    def _initialize_session(self):
        """Perform the MCP initialize handshake."""
        payload = _make_initialize_request()
        result = self._send_mcp_request(payload, "MCP initialize (auth)")
        if result and not isinstance(result, dict):
            return

        self._initialized = True

        # Send initialized notification (no response expected, but we
        # track the request for latency metrics)
        notification = {
            "jsonrpc": JSONRPC_VERSION,
            "method": "notifications/initialized",
        }
        self._send_mcp_request(notification, "MCP initialized notification")

