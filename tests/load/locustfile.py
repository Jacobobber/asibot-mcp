_custom_metrics: dict[str, list[float]] = {
    "auth_latency": [],
    "tool_call_latency": [],
    "rate_limit_hits": [],
}


@events.request.add_listener
def _on_request(request_type, name, response_time, response_length, exception, **kwargs):
    """Classify every request into custom metric buckets."""
    if exception is None:
        if "auth" in name.lower() or "initialize" in name.lower():
            _custom_metrics["auth_latency"].append(response_time)
        elif "tool_call" in name.lower():
            _custom_metrics["tool_call_latency"].append(response_time)
    # Track rate-limit responses (HTTP 429 or MCP-level rate limit errors)
    response = kwargs.get("response")
    if response is not None and response.status_code == 429:
        _custom_metrics["rate_limit_hits"].append(response_time)


def _percentile(values: list[float], pct: float) -> float:
    """Calculate the given percentile from a sorted list of values."""
    if not values:
        return 0.0
    idx = int(len(values) * pct)
    idx = min(idx, len(values) - 1)
    return values[idx]


@events.test_stop.add_listener
def _on_test_stop(environment, **kwargs):
    """Print a summary of custom metrics and benchmark pass/fail when the test ends."""
    print("\n" + "=" * 70)
    print("CUSTOM METRICS SUMMARY")
    print("=" * 70)
    for metric_name, values in _custom_metrics.items():
        if not values:
            print(f"  {metric_name}: no data")
            continue
        count = len(values)
        avg = sum(values) / count
        sorted_vals = sorted(values)
        p50 = _percentile(sorted_vals, 0.50)
        p95 = _percentile(sorted_vals, 0.95)
        p99 = _percentile(sorted_vals, 0.99)
        print(
            f"  {metric_name}: count={count}  avg={avg:.1f}ms  "
            f"p50={p50:.1f}ms  p95={p95:.1f}ms  p99={p99:.1f}ms"
        )
    print("=" * 70)

    # --- Benchmark validation ---
    print("\n" + "=" * 70)
    print("BENCHMARK VALIDATION")
    print("=" * 70)

    checks: list[tuple[str, bool, str]] = []

    # Throughput: aggregate RPS from Locust stats
    stats = environment.runner.stats if environment.runner else None
    total_rps = 0.0
    total_reqs = 0
    total_fails = 0
    if stats:
        total = stats.total
        total_rps = total.current_rps if hasattr(total, "current_rps") else 0
        total_reqs = total.num_requests
        total_fails = total.num_failures

    checks.append((
        "Throughput >= 500 req/s",
        total_rps >= 500,
        f"{total_rps:.1f} req/s",
    ))

    # Auth latency p95 < 100ms
    auth_vals = sorted(_custom_metrics.get("auth_latency", []))
    auth_p95 = _percentile(auth_vals, 0.95) if auth_vals else 0
    checks.append((
        "Auth latency p95 < 100ms",
        auth_p95 < 100 if auth_vals else False,
        f"{auth_p95:.1f}ms" if auth_vals else "no data",
    ))

    # Tool call latency p95 < 200ms
    tool_vals = sorted(_custom_metrics.get("tool_call_latency", []))
    tool_p95 = _percentile(tool_vals, 0.95) if tool_vals else 0
    checks.append((
        "Tool call latency p95 < 200ms",
        tool_p95 < 200 if tool_vals else False,
        f"{tool_p95:.1f}ms" if tool_vals else "no data",
    ))

    # Error rate < 1%
    error_rate = (total_fails / total_reqs * 100) if total_reqs > 0 else 100
    checks.append((
        "Error rate < 1%",
        error_rate < 1,
        f"{error_rate:.2f}%",
    ))

    all_passed = True
    for label, passed, value in checks:
        status = "PASS" if passed else "FAIL"
        if not passed:
            all_passed = False
        print(f"  [{status}] {label}: {value}")

    print("=" * 70)
    if all_passed:
        print("RESULT: ALL BENCHMARKS PASSED")
    else:
        print("RESULT: SOME BENCHMARKS FAILED")
    print("=" * 70 + "\n")


