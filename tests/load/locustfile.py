_CSV_PATH = os.environ.get(
    "LOCUST_USERS_CSV",
    os.path.join(os.path.dirname(__file__), "test_users.csv"),
)

_seeded_keys: list[dict[str, str]] | None = None


def _load_seeded_keys() -> list[dict[str, str]]:
    """Load pre-seeded user_id,api_key pairs from CSV.

    Falls back to generating fake keys if the CSV does not exist (legacy mode),
    but prints a loud warning since those requests will get auth errors.
    """
    global _seeded_keys
    if _seeded_keys is not None:
        return _seeded_keys

    if os.path.exists(_CSV_PATH):
        with open(_CSV_PATH, newline="") as f:
            reader = csv.DictReader(f)
            _seeded_keys = [row for row in reader if row.get("api_key")]
        logger.info("Loaded %d pre-seeded API keys from %s", len(_seeded_keys), _CSV_PATH)
    else:
        logger.warning(
            "No pre-seeded users CSV found at %s. "
            "Generating fake API keys -- most requests will fail auth. "
            "Run seed_test_users.py first for realistic testing.",
            _CSV_PATH,
        )
        _seeded_keys = [
            {"user_id": f"fake-{i}@test.example.com", "api_key": f"asb_{''.join(random.choices(string.ascii_letters + string.digits, k=43))}"}
            for i in range(200)
        ]

    return _seeded_keys


