MCP_PATH = "/mcp"
JSONRPC_VERSION = "2.0"


def _jsonrpc_request(method: str, params: dict | None = None, req_id: int | None = None) -> dict:
    """Build a JSON-RPC 2.0 request envelope."""
    msg: dict[str, Any] = {
        "jsonrpc": JSONRPC_VERSION,
        "method": method,
    }
    if params is not None:
        msg["params"] = params
    if req_id is not None:
        msg["id"] = req_id
    else:
        msg["id"] = random.randint(1, 2**31)
    return msg


def _make_tool_call(tool_name: str, arguments: dict | None = None, req_id: int | None = None) -> dict:
    """Build a JSON-RPC tools/call request."""
    return _jsonrpc_request(
        "tools/call",
        params={"name": tool_name, "arguments": arguments or {}},
        req_id=req_id,
    )


def _make_initialize_request(req_id: int | None = None) -> dict:
    """Build an MCP initialize request."""
    return _jsonrpc_request(
        "initialize",
        params={
            "protocolVersion": "2025-03-26",
            "capabilities": {},
            "clientInfo": {
                "name": "locust-load-test",
                "version": "1.0.0",
            },
        },
        req_id=req_id,
    )


def _make_list_tools_request(req_id: int | None = None) -> dict:
    """Build an MCP tools/list request."""
    return _jsonrpc_request("tools/list", req_id=req_id)


