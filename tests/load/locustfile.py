class UnauthenticatedUser(HttpUser):
    """Simulates users with no API key or invalid credentials.

    Verifies the server handles auth failures gracefully under load
    without leaking resources or crashing.
    """

    weight = 1  # 1 unauthenticated for every 10 authenticated (see McpUser weight)
    wait_time = between(2.0, 8.0)

    def _send_unauthenticated(self, payload: dict, name: str, headers: dict | None = None):
        """Send a request without valid auth."""
        default_headers = {
            "Content-Type": "application/json",
            "Accept": "application/json, text/event-stream",
        }
        if headers:
            default_headers.update(headers)

        with self.client.post(
            MCP_PATH,
            json=payload,
            headers=default_headers,
            name=name,
            catch_response=True,
        ) as response:
            # We expect 401/403 or a JSON-RPC error -- both are "correct"
            if response.status_code in (401, 403):
                response.success()
            elif response.status_code == 429:
                _custom_metrics["rate_limit_hits"].append(response.elapsed.total_seconds() * 1000)
                response.success()
            elif response.status_code < 400:
                # Some endpoints (health) may succeed without auth
                response.success()
            else:
                response.failure(f"Unexpected {response.status_code}")

    @task(5)
    @tag("auth", "no_key")
    def no_api_key(self):
        """Request with no Authorization header."""
        payload = _make_tool_call("asibot_whoami")
        self._send_unauthenticated(payload, "auth: no_key")

    @task(5)
    @tag("auth", "bad_key")
    def invalid_api_key(self):
        """Request with a malformed API key."""
        bad_key = "invalid_" + "".join(random.choices(string.ascii_letters, k=20))
        payload = _make_tool_call("asibot_whoami")
        self._send_unauthenticated(
            payload,
            "auth: invalid_key",
            headers={"Authorization": f"Bearer {bad_key}"},
        )

    @task(2)
    @tag("auth", "brute_force")
    def brute_force_auth(self):
        """Rapid auth attempts with random keys -- should trigger rate limiting."""
        for _ in range(12):
            bad_key = f"asb_{''.join(random.choices(string.ascii_letters, k=43))}"
            payload = _make_tool_call("asibot_whoami")
            self._send_unauthenticated(
                payload,
                "auth: brute_force_attempt",
                headers={"Authorization": f"Bearer {bad_key}"},
            )

    @task(3)
    @tag("auth", "health")
    def health_no_auth(self):
        """Health check without auth -- should succeed."""
        payload = _make_tool_call("asibot_health")
        self._send_unauthenticated(payload, "auth: health_no_auth")


# ---------------------------------------------------------------------------
# SSE response parser
# ---------------------------------------------------------------------------


def _parse_sse_response(body: str) -> dict | None:
    """Extract the last JSON-RPC result from an SSE event stream.

    MCP streamable-http returns events like:
        event: message
        data: {"jsonrpc": "2.0", "id": 1, "result": {...}}

    We parse and return the last data payload that contains a result.
    """
    last_result = None
    for line in body.split("\n"):
        line = line.strip()
        if line.startswith("data:"):
            data_str = line[5:].strip()
            if not data_str:
                continue
            try:
                parsed = json.loads(data_str)
                if isinstance(parsed, dict):
                    last_result = parsed
            except json.JSONDecodeError:
                continue
    return last_result


# ---------------------------------------------------------------------------
# Step load shape: ramp from 10 to 1000 users over 5 minutes
# ---------------------------------------------------------------------------


class StepLoadShape(LoadTestShape):
    """Ramp-up configuration:

    Phase 1 (0-1 min):   10 -> 100 users   (warm-up)
    Phase 2 (1-2 min):   100 -> 300 users   (moderate load)
    Phase 3 (2-3 min):   300 -> 600 users   (heavy load)
    Phase 4 (3-4 min):   600 -> 900 users   (near peak)
    Phase 5 (4-5 min):   900 -> 1000 users  (full load)
    Phase 6 (5-10 min):  1000 users          (sustained peak)
    Phase 7 (10-11 min): 1000 -> 0 users     (cool-down)
    """

    stages = [
        {"duration": 60, "users": 100, "spawn_rate": 2},      # warm-up
        {"duration": 120, "users": 300, "spawn_rate": 4},     # moderate
        {"duration": 180, "users": 600, "spawn_rate": 5},     # heavy
        {"duration": 240, "users": 900, "spawn_rate": 5},     # near peak
        {"duration": 300, "users": 1000, "spawn_rate": 2},    # full load
        {"duration": 600, "users": 1000, "spawn_rate": 1},    # sustained
        {"duration": 660, "users": 0, "spawn_rate": 20},      # cool-down
    ]

    def tick(self):
        run_time = self.get_run_time()

        for stage in self.stages:
            if run_time < stage["duration"]:
                return (stage["users"], stage["spawn_rate"])

        return None  # Stop the test


